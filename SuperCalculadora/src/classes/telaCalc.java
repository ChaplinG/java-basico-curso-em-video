    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        panCalc.setVisible(true);
        
        int num = Integer.parseInt(txtNum.getValue().toString());
        
        int res = num%2;        
        lblResRes.setText(Integer.toString(res));
        
        double pow = Math.pow(num,3);
        lblResPow.setText(Double.toString(pow));
        
        double sqrt = Math.sqrt(num);
        lblResSqrt.setText(String.format("%.2f", sqrt));
        
        double cbrt = Math.cbrt(num);
        lblResCbrt.setText(String.format("%.2f", cbrt));
        
        int abs = Math.abs(num);
        lblResAbs.setText(Integer.toString(abs));
    }//GEN-LAST:event_jButton1ActionPerformed

